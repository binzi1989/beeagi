            "    ts = datetime.now(timezone.utc).isoformat()\n"
            "    plan_block = \"\\\\n\".join(f\"- {item}\" for item in plan)\n"
            "    return (\n"
            "        f\"# Delivery Report\\n\\n\"\n"
            "        f\"Generated at: {ts}\\n\\n\"\n"
            "        f\"## Goal\\n{goal}\\n\\n\"\n"
            "        f\"## Execution Plan\\n{plan_block}\\n\\n\"\n"
            "        f\"## Execution Summary\\n{summary}\\n\\n\"\n"
