            {"path": f"{project_dir}/src/{pkg}/__init__.py", "kind": "code", "description": "Package init", "content": init_py},
            {"path": f"{project_dir}/src/{pkg}/planner.py", "kind": "code", "description": "Planning and report rendering", "content": planner_py},
            {"path": f"{project_dir}/src/{pkg}/main.py", "kind": "code", "description": "Executable entrypoint", "content": main_py},
